import { createPluginInitializer } from '../plugin-initializer.js';
import { createSettingsTabsManager } from '../ui/settings-tabs-manager.js';
import { createAuthorVerificationManager } from '../utils/author-verification.js';

export function createPluginBootstrapComposition({
  extensionFolderPath,
  pluginAuthor,
  authorVerificationPassword,
  getDisplayedAuthor,
  exportDiaryData,
  importDiaryData,
  toggleFloatWindow,
  resetFloatWindowPosition,
  configurePresets,
  loadSettings,
  loadPluginSettingsStyle,
  loadExchangeDiaryCSS,
  createFloatWindow,
  createCustomCharacterDialog,
  createExchangeDiaryDialog,
  createPresetDialog,
  createDiaryBookDialog,
  createReadmeDialog,
  createSaveSuccessDialog,
  createRecycleBinDialog,
  loadPresetData,
  bindCustomCharacterDialogEvents,
  bindExchangeDiaryDialogEvents,
  bindDiaryBookDialogEvents,
  bindReadmeDialogEvents,
  bindSaveSuccessDialogEvents,
  getCurrentSettings,
  checkAndTriggerAutoDiary,
  triggerManager,
  initializeFileStorage,
  migrateExchangeDiaryData,
  bindUpdateNotificationEvents,
  checkAndShowUpdateNotification,
  notify,
}) {
  const { bindSettingsTabEvents } = createSettingsTabsManager();

  const {
    verifyAuthorInfo,
    buildViolationToastContent: getAuthorViolationToastContent,
  } = createAuthorVerificationManager({
    pluginAuthor,
    authorVerificationPassword,
    getDisplayedAuthor,
  });

  return createPluginInitializer({
    extensionFolderPath,
    pluginAuthor,
    verifyAuthorInfo,
    getAuthorViolationToastContent,
    exportDiaryData,
    importDiaryData,
    toggleFloatWindow,
    resetFloatWindowPosition,
    configurePresets,
    bindSettingsTabEvents,
    loadSettings,
    loadPluginSettingsStyle,
    loadExchangeDiaryCSS,
    createFloatWindow,
    createCustomCharacterDialog,
    createExchangeDiaryDialog,
    createPresetDialog,
    createDiaryBookDialog,
    createReadmeDialog,
    createSaveSuccessDialog,
    createRecycleBinDialog,
    loadPresetData,
    bindCustomCharacterDialogEvents,
    bindExchangeDiaryDialogEvents,
    bindDiaryBookDialogEvents,
    bindReadmeDialogEvents,
    bindSaveSuccessDialogEvents,
    getCurrentSettings,
    checkAndTriggerAutoDiary,
    triggerManager,
    initializeFileStorage,
    migrateExchangeDiaryData,
    bindUpdateNotificationEvents,
    checkAndShowUpdateNotification,
    notify,
  });
}
